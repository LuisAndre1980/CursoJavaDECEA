package aula20;

public class OperadorIncrementoDecremento {

	public static void main(String[] args) {
		
		int idade = 20;
		int idade2 = 20;
		
		//idade += 1;
		
		//idade ++;
		int incremento = ++idade;
		int decremento = idade2 --;
		System.out.println(idade);
		System.out.println(incremento);
		System.out.println(decremento);

	}

}
